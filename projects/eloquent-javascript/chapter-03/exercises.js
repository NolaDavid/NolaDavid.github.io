////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(a,b) {
return a < b ? a : b
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
 return num % 2 === 0 ? true: false
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
let counted = 0;
for(let i = 0; i < string.length; i++){
  if(string[i] === char){
    counted += 1;
  }
}
return counted;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
let counted = 0;
for(let i = 0; i < string.length; i++){
  if(string[i] === "B"){
    counted += 1;
  }
}
return counted
}

